/**
 * Thruple of city info data.
 * */
public class CoordinateInfo {

    public int x;
    public int y;
    public String name;

    public CoordinateInfo() {

    }

    public CoordinateInfo( int x, int y, String name ) {
        this.x = x;
        this.y = y;
        this.name = name;
    }
}
